// =========================================================
// COMPONENT
// =========================================================

#ifndef COMPONENT_H
#define COMPONENT_H

// =========================================================
// COMPONENT
// =========================================================

class Component {
public:
	virtual ~Component() {};
	virtual void setup() = 0;
};

// =========================================================
// INPUT
// =========================================================

class Input: public Component {
public:
	virtual void read() = 0;
};

// =========================================================
// OUTPUT
// =========================================================

class Output: public Component {
public:
	virtual void write() = 0;
};

// =========================================================
// PROCESS
// =========================================================

class Process: public Component {
public:
	virtual void process() = 0;
};

// =========================================================
// FULL PROCESS
// =========================================================

class FullProcess: public Input, Output, Process {
public:
};

#endif

